package Loops;

public class intro {
    public static void main(String[] args) {

        //For Loop

        for(int i = 0; i < 10;i++){
            System.out.print(i + 1);
        }

        System.out.println(" ");
        //While loop

        int j = 0;
        while(j < 5){
            System.out.println("Hello world");
            j++;
        }


    }
}
